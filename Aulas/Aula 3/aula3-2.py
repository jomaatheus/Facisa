'''
Else, em português, significa SENÂO. É usado para executar um
bloco de código caso a condição do IF seja falsa
'''

variavelA=10
variavelB=20

if variavelA > variavelB:
    print('variavel A é maior')
else:
    print('variavel B é maior')

variavelA=20
variavelB=10

if variavelA > variavelB:
    print('variavel A é maior')
else:
    print('variavel B é maior')


